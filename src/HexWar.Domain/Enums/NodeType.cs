namespace HexWar.Domain.Enums;

public enum NodeType
{
    OutPost,         // 일반 점령 대상 
    Chokepoint,     // 요충지 (연결도 3 이상)
    SupplyLine,     //보급로 (점령 시 고정 수비대 1기 제공)
    Headquarters    //본부 (점령 불가, 전체 시야)
}

// 매커니즘 정의 

/* 전략적 의미: N3은 중앙에 위치해 양 진영의 경쟁이 가장 치열할 곳
→ 보급로 특성을 부여해 점령 가치를 높이고 방어적 이점 제공 */

/* 보급로 노드 특성
├── 점령 즉시 고정 수비대 1기 자동 배치
├── 고정 수비대는 이동 불가 (항상 노드 방어)
├── 고정 수비대는 점유권 계산에 포함됨
├── 노드 재점령 시 고정 수비대는 소멸 후 새 주인에게 재생성
└── 일반 유닛과 별도로 표시되어야 함 */